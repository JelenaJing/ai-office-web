function slugTitle(name) {
  return name.replace(/[\\/:*?"<>|]/g, '_')
}

function parseClassic(raw) {
  const [name, author, genre, focus, topics, teaching] = raw.split('|')
  return { name, author, genre, focus, topics, teaching }
}

function parseScientific(raw) {
  const [name, problem, method, experiment, result, limitation] = raw.split('|')
  return { name, problem, method, experiment, result, limitation }
}

