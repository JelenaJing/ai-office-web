async function fetchFiles(partition) {
  const res = await fetch(`${baseUrl}/knowledge-bases/${encodeURIComponent(partition)}/files`)
  if (!res.ok) throw new Error(`list failed ${partition}: ${res.status}`)
  const data = await res.json()
  return data.files || []
}

async function uploadFiles(partition, filePaths) {
  if (!filePaths.length) {
    console.log(`[upload] ${partition}: nothing to upload`)
    return { ok: true, results: [], errors: [] }
  }
  const form = new FormData()
  form.append('partition', partition)
  for (const fp of filePaths) {
    form.append('files', new Blob([fs.readFileSync(fp)]), path.basename(fp))
  }
  const res = await fetch(`${baseUrl}/ingest`, {
    method: 'POST',
    headers: { 'X-KB-Partition': partition },
    body: form,
  })
  if (!res.ok) {
    const text = await res.text()
    throw new Error(`upload failed ${partition}: ${res.status} ${text}`)
  }
  return res.json()
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true })
}

function prepareBatch(rawItems, parser, buildContent, partitionDir, existingNames, neededCount, suffix) {
  const items = rawItems.map(parser)
  const missing = items.filter((item) => !existingNames.has(`${slugTitle(item.name)}${suffix}`)).slice(0, neededCount)
  const filePaths = []
  for (const item of missing) {
    const fileName = `${slugTitle(item.name)}${suffix}`
    const filePath = path.join(partitionDir, fileName)
    fs.writeFileSync(filePath, buildContent(item), 'utf-8')
    filePaths.push(filePath)
  }
  return { selectedCount: missing.length, filePaths }
}

async function handlePartition({ partition, displayName, rawItems, parser, buildContent, dirName, suffix }) {
  ensureDir(path.join(rootDir, dirName))
  const before = await fetchFiles(partition)
  const existingNames = new Set(before.map((item) => item.display_name || item.file_id))
  const need = Math.max(0, targetCount - before.length)
  if (need === 0) {
    console.log(`[target] ${partition}: already at ${before.length}`)
    return { beforeCount: before.length, afterCount: before.length, uploaded: 0 }
  }
  const prepared = prepareBatch(rawItems, parser, buildContent, path.join(rootDir, dirName), existingNames, need, suffix)
  if (prepared.selectedCount < need) {
    throw new Error(`${partition} still needs ${need} files, but only ${prepared.selectedCount} unique planned files available`)
  }
  const uploadResult = await uploadFiles(partition, prepared.filePaths)
  console.log(`[upload] ${partition} (${displayName}): planned=${need}, uploaded=${prepared.filePaths.length}`)
  console.log(JSON.stringify(uploadResult, null, 2))
  const after = await fetchFiles(partition)
  console.log(`[count] ${partition}: ${before.length} -> ${after.length}`)
  return { beforeCount: before.length, afterCount: after.length, uploaded: prepared.filePaths.length }
}

async function main() {
  const classic = await handlePartition({
    partition: 'classic-reading',
    displayName: '经典阅读',
    rawItems: classicItems,
    parser: parseClassic,
    buildContent: classicContent,
    dirName: 'classic',
    suffix: '.md',
  })
  const scientific = await handlePartition({
    partition: 'scientific-papers',
    displayName: '科技论文',
    rawItems: scientificItems,
    parser: parseScientific,
    buildContent: scientificContent,
    dirName: 'scientific',
    suffix: '.md',
  })

  const classicFiles = await fetchFiles('classic-reading')
  const scientificFiles = await fetchFiles('scientific-papers')
  console.log(`[done] classic-reading total=${classicFiles.length}`)
  console.log(`[done] scientific-papers total=${scientificFiles.length}`)
  console.log('[tail] classic-reading latest:')
  for (const item of classicFiles.slice(-12)) console.log(` - ${item.display_name || item.file_id}`)
  console.log('[tail] scientific-papers latest:')
  for (const item of scientificFiles.slice(-12)) console.log(` - ${item.display_name || item.file_id}`)

  if (classicFiles.length !== targetCount || scientificFiles.length !== targetCount) {
    throw new Error(`target not reached: classic=${classicFiles.length}, scientific=${scientificFiles.length}`)
  }
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
